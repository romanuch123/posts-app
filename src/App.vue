    users() {
      return this.$store.getters.USERS;
    },
  },
  methods: {
  },
  async created() {
    await this.$store.dispatch('GET_USERS');
    const authUserEmail = localStorage.getItem('authUserEmail');
    if (authUserEmail) {
      const existingUser = this.users.find((el) => el.email === authUserEmail);
      this.$store.dispatch('AUTH_USER_REQUEST', existingUser);
    }
  },
};
